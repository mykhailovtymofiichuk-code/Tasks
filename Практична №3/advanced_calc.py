# Обчислення коренів через оператор піднесення до степеня
sqrt_144 = 144 ** 0.5  # Квадратний корінь — це степінь 0.5
cube_root_27 = 27 ** (1/3)  # Кубічний корінь — це степінь 1/3

print(f"Квадратний корінь 144 = {sqrt_144}") # Вивід квадратного кореня
print(f"Кубічний корінь 27 = {round(cube_root_27, 2)}") # Вивід з округленням

# Геометрія: Діагональ прямокутника за теоремою Піфагора
# Формула: d = sqrt(a^2 + b^2)
w = 12 # Ширина
h = 5  # Висота
diagonal = (w**2 + h**2) ** 0.5 # Розрахунок діагоналі
print(f"Діагональ прямокутника: {diagonal}") # Вивід результату

# Калькулятор знижок
item1, item2, item3 = 150, 240, 110 # Ціни трьох різних товарів
discount_rate = 0.25 # Знижка 25% (у форматі десяткового дробу)

subtotal = item1 + item2 + item3 # Загальна сума без знижки
savings = subtotal * discount_rate # Розрахунок суми економії
final_price = subtotal - savings # Сума до сплати після знижки

print("-" * 30) # Лінія для чеку
print(f"Сума без знижки: {subtotal} грн") # Вивід чеку
print(f"Ваша економія: {savings} грн")   # Скільки клієнт зберіг
print(f"Разом до сплати: {final_price} грн") # Кінцевий результат
print("-" * 30) # Лінія для чеку