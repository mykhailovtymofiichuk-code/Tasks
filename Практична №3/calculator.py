a = 20  # Ініціалізація змінної a значенням 20
b = 6   # Ініціалізація змінної b значенням 6

print("=" * 30)  # Вивід декоративної лінії для оформлення
print(f"{a} + {b} = {a + b}")    # Додавання та вивід результату
print(f"{a} - {b} = {a - b}")    # Віднімання та вивід результату
print(f"{a} * {b} = {a * b}")    # Множення та вивід результату
print(f"{a} / {b} = {a / b}")    # Ділення (результат — число з плаваючою точкою)
print(f"{a} // {b} = {a // b}")  # Цілочисельне ділення
print(f"{a} % {b} = {a % b}")    # Остача від ділення
print(f"{a} ** {b} = {a ** b}")  # Піднесення до степеня
print("=" * 30)  # Вивід декоративної лінії

# Крок 5: Складені оператори
score = 0  # Початкове значення лічильника
print(f"Початковий score: {score}") # Вивід початкового значення

score += 10  # Збільшення score на 10
print(f"score += 10: {score}")      # Вивід поточного значення

score += 5   # Додавання 5 до поточного значення
print(f"score += 5: {score}")       # Вивід поточного значення

score -= 3   # Віднімання 3 від поточного значення
print(f"score -= 3: {score}")       # Вивід поточного значення

score *= 2   # Множення поточного значення на 2
print(f"Фінальний score (*= 2): {score}") # Вивід фінального прогресу
print("=" * 30)  # Декоративна лінія

# Крок 6: Вбудовані функції
total = 99.99 * 1.20            # Розрахунок загальної суми (ціна з націнкою)
rounded_total = round(total, 2) # Округлення результату до 2 знаків після коми
print(f"Округлена сума: {rounded_total}") # Вивід результату округлення

math_grade = 85   # Змінна з оцінкою за математику
science_grade = 92 # Змінна з оцінкою за науку
art_grade = 78     # Змінна з оцінкою за мистецтво

best_grade = max(math_grade, science_grade, art_grade) # Пошук максимального значення
worst_grade = min(math_grade, science_grade, art_grade) # Пошук мінімального значення

print(f"Найкраща оцінка: {best_grade}") # Вивід найкращого результату
print(f"Найгірша оцінка: {worst_grade}") # Вивід найгіршого результату