print("-" * 21)      # Розділювач

celsius = 25 # Початкове значення в градусах Цельсія

# Перетворення Цельсія у Фаренгейт
fahrenheit = celsius * 9/5 + 32 # Розрахунок за стандартною формулою
print(f"{celsius}°C дорівнює {fahrenheit}°F") # Вивід прямої конвертації

# Зворотне перетворення
celsius_back = (fahrenheit - 32) * 5/9 # Формула для отримання Цельсія з Фаренгейта
print(f"Зворотна конвертація: {celsius_back}°C") # Вивід результату повернення