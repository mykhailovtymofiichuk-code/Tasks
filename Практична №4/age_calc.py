name = input("Введіть ім'я користувача: ")
years = int(input("Введіть кількість повних років: "))

# Розрахунок на основі стандартних значень (місяці та дні)
months = years * 12
days = years * 365

# Структуроване виведення "картки"
print("-" * 20)
print(f"КОРИСТУВАЧ: {name}")
print(f"Місяців прожито: {months}")
print(f"Днів прожито: {days}")
print("-" * 20)