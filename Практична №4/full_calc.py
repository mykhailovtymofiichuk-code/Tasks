a = float(input("Число A: "))
b = float(input("Число B: "))

print(f"Додавання: {a + b}")
print(f"Віднімання: {a - b}")
print(f"Множення: {a * b}")
print(f"Ділення (звичайне): {a / b}")
print(f"Цілочисельне ділення (скільки разів 'b' входить в 'a'): {a // b}")
print(f"Остача від ділення: {a % b}")
print(f"Піднесення до степеня: {a ** b}")