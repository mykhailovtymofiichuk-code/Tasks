score = int(input("Бал (0-100): "))

# Перевірка через каскад умов (важливо дотримуватися порядку від більшого до меншого)
if 90 <= score <= 100:
    print("Відмінно")
elif 75 <= score < 90:
    print("Добре")
elif 60 <= score < 75:
    print("Задовільно")
elif 0 <= score < 60:
    print("Незадовільно")
else:
    print("Некоректний бал!")