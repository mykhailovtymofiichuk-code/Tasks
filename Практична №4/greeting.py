# Отримуємо дані від користувача (input завжди повертає рядок)
name = input("Введіть ваше ім'я: ")
# Одразу перетворюємо введений текст на ціле число (int)
age = int(input("Введіть ваш вік: "))

# Проста математична операція
next_year_age = age + 1

# Використовуємо f-рядки для зручного вставлення змінних у текст
print("=" * 30) # Декоративна лінія
print(f"Привіт, {name}!")
print(f"Зараз вам {age} років.")
print(f"Наступного року вам буде {next_year_age}!")
print("=" * 30)