# Використовуємо float, щоб користувач міг вводити числа з крапкою (напр. 5.5)
num1 = float(input("Введіть перше число: "))
num2 = float(input("Введіть друге число: "))

# Обчислюємо суму
result = num1 + num2

# Виведення результату у форматі математичного рівняння
print(f"{num1} + {num2} = {result}")